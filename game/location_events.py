def get_event_text(location_slug: str) -> str:
    """Возвращает случайный текст события для локации."""
    events = LOCATION_EVENTS.get(location_slug, GENERIC_EVENTS)
    return random.choice(events)


def get_weighted_event(location_slug: str, event_from_pool: dict | None) -> str:
    """
    50% шанс уникального текста локации, 50% — из игрового пула.
    Если пул пуст — берём уникальный текст.
    """
    if event_from_pool and event_from_pool.get("text") and random.random() > 0.5:
        return event_from_pool["text"]
    return get_event_text(location_slug)
